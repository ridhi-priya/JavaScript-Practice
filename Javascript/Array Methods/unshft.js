// The unshift() method in JavaScript is used to add one or more elements to the beginning of an array.
// It modifies the original array and returns the new length of the array.
// array.unshift(element1, element2, ..., elementN)

var fruits = ["mango", "kiwi", "pears", "apple", "Banana", "papaya"];
console.log(fruits.length);
fruits.unshift("Grapes", "pine apple");

console.log(fruits.length);
console.log(fruits);
