// The shift() method in JavaScript is used to remove the first element from an array.
// It modifies the original array and returns the removed element.

var fruits = ["mango", "kiwi", "pears", "apple", "Banana", "papaya"];
console.log(fruits.length);
const delFruits = fruits.shift("apple", "banana");

console.log(fruits.length);
console.log(delFruits);
